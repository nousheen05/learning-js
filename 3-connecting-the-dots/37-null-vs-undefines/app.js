// Null and Undefined
// both represent "no value"

// undefined - "javascript can not find value for this"
// ex: variable without value
// missing function arguments
// missing object properties

// null - "developer sets the value(to take no value)"

let a = 2 + null; //20+0
console.log(a);
let b = 2 + undefined; // 20+0
console.log(b);